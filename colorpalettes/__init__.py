def add_palette(name:Hashable, colors:List[Tuple[int, int, int]]):
	palettes[name] = colors

def get_palette(name:Hashable) -> List[Tuple[int, int, int]]:
	if name in palettes:
		return palettes[name]
	p = '\n'.join(palettes.keys())
	raise IndexError(f'No palette found by the name "{name}".  Currently mapped palettes:\n{p}')

def scale_value(c:Tuple[int, int, int], t:float) -> Tuple[int, int, int]:
	r,g,b = c
	if r == 0 and g == 0 and b == 0:
		return 0, 0, 0
	if r==0 and b==0 and c==0:
		return 0,0,0
	t = min(max(t,0),255./max(r,g,b))
	return int(r * t), int(g * t), int(b * t)

def scale_saturation(c:Tuple[int, int, int], t:float) -> Tuple[int, int, int]:
	r,g,b = c
	l = min(r,g,b)
	h = max(r,g,b)
	d = h-l
	t = min(max(t,0),h/d)
	_h = h*(1-t)
	return int(_h+r*t), int(_h+g*t), int(_h+b*t)

def set_saturation(c:Tuple[int, int, int], t:float) -> Tuple[int, int, int]:
	r,g,b = c
	h,_,v = rgb_to_hsv(r/255.,g/255.,b/255.)
	r,g,b = hsv_to_rgb(h, t, v)
	return int(r*255.+.5), int(g*255.+.5), int(b*255.+.5)

def shift_hue(c:Tuple[int, int, int], hue:float) -> Tuple[int, int, int]:
	'''
	Shift the hue of `c` by `hue`.  `hue` ranges from 0-1.
	'''
	if is_a_palette(c):
		return shift_palette_hue(c, hue)
	r,g,b = c
	h,s,v = rgb_to_hsv(r/255., g/255., b/255.)
	r,g,b = hsv_to_rgb(h+hue, s, v)
	return int(r*255), int(g*255), int(b*255)

def shift_palette_hue(palette:List[Tuple[int, int, int]], hue:float) -> List[Tuple[int, int, int]]:
	return [shift_hue(c, hue) for c in palette]

def cycle_palette(palette:List[Tuple[int, int, int]]) -> List[Tuple[int, int, int]]:
	'''
	Return an extended palette which is `palette` followed by the reverse of `palette`.  The endpoints of `palette` are not repeated in the returned palette.
	'''
	return palette + palette[-2:0:-1]

def is_rgb_color(c:Any) -> bool:
	return isinstance(c, tuple) and len(c) == 3 and isinstance(c[0], int) and isinstance(c[1], int) and isinstance(c[2], int)

def is_a_palette(palette:Any) -> bool:
	return isinstance(palette, list) and all(map(is_rgb_color, palette))


