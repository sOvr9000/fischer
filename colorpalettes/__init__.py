class CycleMode(IntEnum):
	Repeat = auto()
	Oscillate = auto()


def add_color(name:Hashable, value:Tuple[int, int, int]) -> None:
	colors[name] = value

def get_color(name:str) -> Tuple[int, int, int]:
	if name in colors:
		return colors[name]
	raise IndexError(f'No color found with name "{name}"')

def get_color_name(rgb:Tuple[int, int, int]) -> str:
	'''
	Get a name of a color from its RGB value.
	'''
	r, g, b = rgb
	lowest_n = None
	lowest_d = 196608
	for n, c in colors.items():
		if lowest_n is None:
			lowest_n = [n]
		else:
			d = (c[0] - r) * (c[0] - r) + (c[1] - g) * (c[1] - g) + (c[2] - b) * (c[2] - b)
			if d < lowest_d:
				lowest_d = d
				lowest_n = [n]
			elif d == lowest_d:
				lowest_n.append(n)
	if len(lowest_n) == 1:
		return lowest_n[0]
	return lowest_n[0] # for now.. later, implement checking for lightness (and pick the closest one)

def convert_palette(palette:List[str]) -> List[Tuple[int, int, int]]:
	'''
	Convert hex color strings to a list of 3-tuples representing RGB format.
	'''
	return [
		tuple(int(h[i:i+2], 16) for i in (1, 3, 5))
		if h.startswith('#') else
		tuple(int(h[i:i+2], 16) for i in (0, 2, 4))
		for h in palette
	]

def get_color_from_palette(palette:List[Tuple[int, int, int]], index:float, cycle_mode:CycleMode=CycleMode.Repeat) -> Tuple[int, int, int]:
	'''
	Automatically map `index` to be no less than `0` and no greater than `len(palette)-1`, and `index` does not have to be an integer.
	'''
	index = int(.5 + index)
	if cycle_mode == CycleMode.Repeat:
		return palette[index%len(palette)]
	elif cycle_mode == CycleMode.Oscillate:
		m = index%len(palette)
		n = index//len(palette)
		if n % 2 == 1:
			m = -m
		return palette[m]
	else:
		return get_color_from_palette(palette, index)

